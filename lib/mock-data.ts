// ─── Sparkline Generator ──────────────────────────────────────────────────────

export function generateSparkline(trend: "up" | "down" | "flat", points = 20): number[] {
  const result: number[] = [];
  let v = 100;
  const drift = trend === "up" ? 0.3 : trend === "down" ? -0.3 : 0;
  for (let i = 0; i < points; i++) {
    v += drift + (Math.random() * 4 - 2);
    result.push(parseFloat(v.toFixed(2)));
  }
  return result;
}
