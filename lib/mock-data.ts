function generatePortfolioHistory(): ChartDataPoint[] {
  const data: ChartDataPoint[] = [];
  let value = 240000;
  let benchmark = 240000;
  const start = new Date("2023-01-02");

  for (let i = 0; i < 370; i++) {
    const d = new Date(start);
    d.setDate(d.getDate() + i);
    if (d.getDay() === 0 || d.getDay() === 6) continue;

    const drift = 0.00055;
    const vol = 0.012;
    const bmVol = 0.008;
    const shock = Math.random();
    const dayReturn = drift + vol * (Math.random() * 2 - 1) + (shock > 0.97 ? 0.04 : shock < 0.03 ? -0.04 : 0);
    const bmReturn = drift * 0.7 + bmVol * (Math.random() * 2 - 1);

    value = value * (1 + dayReturn);
    benchmark = benchmark * (1 + bmReturn);

    data.push({
      date: d.toISOString().split("T")[0],
      value: Math.round(value),
      benchmark: Math.round(benchmark),
    });
  }

  return data;
}

